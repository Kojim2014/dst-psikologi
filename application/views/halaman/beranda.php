	<h1>Teori Dempster-Shafer</h1>
	<p>
	Teori Dempster-Shafer pertama kali  diperkenalkan oleh oleh Arthur P. Dempster and Glenn Shafer, yang melakukan percobaan ketidakpastian dengan range probabilities daripada sebagai probabilitas tunggal. Kemudian pada tahun 1976 Shafer mempublikasikan teori Dempster pada buku yang berjudul Mathematichal Theory of Evident. 
	Teori Dempster-Shafer merupakan teori matematika dari evidence. Teori tersebut dapat memberikan sebuah cara untuk menggabungkan evidence dari beberapa sumber dan mendatangkan atau memberikan tingkat kepercayaan (direpresentasikan melalui fungsi kepercayaan) dimana mengambil dari seluruh evidence yang tersedia. Secara umum Teori Dempster-Shafer ditulis dalam suatu interval yaitu [Belief,Plausibility]
